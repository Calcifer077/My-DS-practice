import java.util.*;

public class _1703E {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t > 0) {
            int n = sc.nextInt();
            String str[] = new String[n];
            for (int i = 0; i < n; i++) {
                str[i] = sc.next();
            }

            t--;
        }
        sc.close();
    }
}